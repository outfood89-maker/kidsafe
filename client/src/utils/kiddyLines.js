const pick = (arr) => arr[Math.floor(Math.random() * arr.length)];
const fill = (tpl, name, answer = "") =>
  tpl.replaceAll("{voc}", withVocative(name)).replaceAll("{name}", name).replaceAll("{answer}", answer);

