export const greetingLine = (name, recentMood) => {
  const key = !recentMood
    ? "first"
    : POSITIVE.includes(recentMood)
      ? "positive"
      : NEGATIVE.includes(recentMood)
        ? "negative"
        : "neutral";
  return fill(pick(GREETING[key]), name);
};

